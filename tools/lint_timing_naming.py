#!/usr/bin/env python3
"""Lint cross-stage accessor naming in the timing model.

Scans every header under `sim/include/gpu_sim/timing/` and reports
violations of the rules documented in
`/resources/cpp_coding_standard.md` § Cross-stage accessor naming and
`/resources/timing_discipline.md`.

Seven checks run in order. The first six are header-checks; the seventh
is a libclang-driven static-analysis pass over the `.cpp` translation
units.

The two state-shape rules introduced with the `reg.h` migration (final
phase of `project-plans/shiny-meandering-pine.md`):

  *No raw current_/next_ field pair* — if a class declares both
  `current_<NAME>_` and `next_<NAME>_` as member fields, the lint
  rejects the pair and points at the canonical primitives in
  `sim/include/gpu_sim/timing/reg.h`. Lone `next_<NAME>_` (e.g.
  `LdStUnit::next_push_` documented staging slot, `MSHREntry::next_in_chain`
  struct link) and lone `current_<NAME>_` are intentionally legal.

  *Every Reg<>/RegFifo<> member must be enrolled* — every
  `Reg<>` / `RegFifo<>` member of a `RegisteredStage`-derived class
  must appear in a `register_state(&<NAME>, ...)` call (in the header's
  inline constructor body OR the corresponding `.cpp` constructor). A
  Reg/RegFifo member whose owning class does NOT derive
  `RegisteredStage` is also flagged (the mixin is mandatory for
  registers). `Wire<>` is exempt: a `Wire` is not a `RegBase`, has no
  committed twin, and is reset/driven inline by its producer.

  *Prefix layer* — every public const member function returning `bool`,
  `std::optional<…>`, a payload struct, or a payload reference must
  start with `current_` or `next_`. Lifecycle hooks (`evaluate`,
  `commit`, `reset`, `flush`, `seed_next`, `accept`, `consume_result`,
  `add_source`, etc.) are exempt via the explicit `LIFECYCLE_METHODS`
  allowlist.

  *Postfix layer* — for each accessor, classify by return type and name
  shape:
    - `bool` and matches `^(current|next)_has_<noun>` → possession
      predicate, OK.
    - `bool` otherwise → expected state predicate; flag any `_is_*` /
      `_has_*` mid-name patterns that suggest the wrong shape.
    - non-`bool` → expected payload accessor; flag adjective tells
      (`_busy`, `_idle`, `_stalled`, `_full`, `_empty`).

  *Polarity layer* — flag pairs of accessors on the same class whose
  names suggest inverse meanings (`busy`/`ready`, `stalled`/`advancing`,
  `full`/`empty` etc.). Only one polarity per concept.

  *Field-shape layer* — fields named `current_*` / `next_*` must be
  declared in a `private:` section. Public bare references to other
  module classes held as members are flagged as candidates for the
  post-wired-pointer rule (human decides).

  *Cross-module-read layer* (Phase 10F) — a libclang pass over the
  timing `.cpp` translation units. It finds every cross-module
  `other->next_*()` member-call read and classifies it by dataflow
  direction against the shared `MODULE_ORDER`:

    - reader upstream of producer  → COMBINATIONAL backward → OK
      (legitimate back-pressure / stall — the expected first-class use
      of a `next_*` cross-module read).
    - reader downstream of producer → COMBINATIONAL forward → VIOLATION
      (collapses pipeline depth; after Phase 10 no such edge is
      legitimate).
    - reader == producer → self-read of an own staging field → exempt.

  There is no allowlist of legitimate back edges — direction is
  structural. Control-cluster orchestrators (`TimingModel`,
  `PanicController`) are exempt as *readers*: they observe arbitrary
  module state from orchestration methods and are not part of the
  linear pipeline dataflow. The one named exception is
  `ExternalMemoryInterface::next_has_response()`, a `next_`-prefixed
  compatibility alias that actually returns committed state — see
  `NEXT_PREFIX_COMMITTED_ALIASES`.

Initial mode is report-only (Phase 0): the script prints findings to
stdout but exits 0. Phase 6 flips the default to enforcement; the
`--report-only` flag preserves the legacy behavior for local
exploration. CI invokes the script without flags.

The cross-module-read layer requires `build/compile_commands.json`
(generated by `cmake -B build`) and the libclang Python bindings. In
default enforcement mode, inability to run this layer fails the build;
use `--report-only` for local exploration or `--skip-cross-module` for
an explicit header-only run.

Per-line annotation `// timing-naming-allow: <reason>` silences a
single finding on the annotated line.
"""

