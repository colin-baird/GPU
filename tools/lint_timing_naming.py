#   The MODULE_ORDER rule would classify the read as "backward / OK"
#   for the wrong reason (it is not a back-pressure signal at all); the
#   alias is exempted explicitly instead of being silently accepted.
#   Documented in resources/timing_discipline.md row 15 and
