# committed end-of-previous-cycle state rather than a live mid-tick
# transient — so a cross-module read of them is NOT a combinational
# edge regardless of dataflow direction. The cross-module-read layer
# skips these with an explicit note.
#
#   ExternalMemoryInterface::next_has_response() is a Phase-M5
#   compatibility alias for current_has_response(). The cache reads it
